#!/usr/bin/env python3
"""Module to concat strings"""


def concat(str1: str, str2: str) -> str:
    """Function to concat strings

    Args:
        str1 (str): [First string]
        str2 (str): [Second string]

    Returns:
        str: [Concatenated string]
    """
    str3 = str1 + str2
    return str3
